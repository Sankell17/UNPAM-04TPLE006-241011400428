    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {

        String namaAyah = jTextField1.getText().trim();
        String namaIbu = jTextField2.getText().trim();
        String noHp = jTextField3.getText().trim();
        String alamat = jTextArea1.getText().trim();

        // Validasi kosong
        if(namaAyah.isEmpty() ||
           namaIbu.isEmpty() ||
           noHp.isEmpty() ||
           alamat.isEmpty()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Semua data harus diisi!");

            return;
        }

        // Validasi
        if(!namaAyah.matches("[a-zA-Z., ]+")) {

            JOptionPane.showMessageDialog(
                    null,
                    "Nama Ayah hanya boleh huruf!");

            return;
        }

        // Validasi
        if(!namaIbu.matches("[a-zA-Z., ]+")) {

            JOptionPane.showMessageDialog(
                    null,
                    "Nama Ibu hanya boleh huruf!");

            return;
        }

        // Validasi no hp
        if(!noHp.matches("\\d{10,13}")) {

            JOptionPane.showMessageDialog(
                    null,
                    "No HP harus 10 - 13 digit dan berupa angka!");

            return;
        }


        Connection conn = koneksi.getConnection();

        String sql = "INSERT INTO orang_tua "
                + "(nama_ayah,nama_ibu,no_hp,alamat) "
                + "VALUES (?,?,?,?)";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, jTextField1.getText());
        pst.setString(2, jTextField2.getText());
        pst.setString(3, jTextField3.getText());
        pst.setString(4, jTextArea1.getText());

        pst.executeUpdate();

        JOptionPane.showMessageDialog(
                null,
                "Data Berhasil Disimpan");

        tampilData();

        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextArea1.setText("");

        jTextField1.requestFocus();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Data Gagal Disimpan : "
                + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

