    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {

        String namaKelas = jTextField1.getText().trim();
        String jadwal = jTextField2.getText().trim();
        String pengajar = jTextField3.getText().trim();

        if(namaKelas.isEmpty() ||
           jadwal.isEmpty() ||
           pengajar.isEmpty()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Semua data harus diisi!");

            return;
        }

        if(!pengajar.matches("[a-zA-Z., ]+")) {

            JOptionPane.showMessageDialog(
                    null,
                    "Nama Pengajar hanya boleh huruf!");

            return;
        }

        Connection conn = koneksi.getConnection();

        String sql = "INSERT INTO kelas "
                + "(nama_kelas,jadwal,pengajar) "
                + "VALUES (?,?,?)";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, namaKelas);
        pst.setString(2, jadwal);
        pst.setString(3, pengajar);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(
                null,
                "Data Berhasil Disimpan");

        tampilData();

        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Data Gagal Disimpan : "
                + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

